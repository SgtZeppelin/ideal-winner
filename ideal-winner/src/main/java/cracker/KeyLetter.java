package cracker;

/**
 *
 * @author gersc
 */
public class KeyLetter {
    
    private char a;
    private char b;

    public KeyLetter(char a, char b) {
        this.a = a;
        this.b = b;
    }

    public char getA() {
        return a;
    }

    public char getB() {
        return b;
    }

    public void setB(char b) {
        this.b = b;
    }
    
    
    
    
}
