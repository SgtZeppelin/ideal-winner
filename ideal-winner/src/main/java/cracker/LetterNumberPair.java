package cracker;

/**
 *
 * @author gersc
 */
public class LetterNumberPair {
    private char letter;
    private int number;

    public LetterNumberPair(char letter, int number) {
        this.letter = letter;
        this.number = number;
    }

    public char getLetter() {
        return letter;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }
    
    
    
}
